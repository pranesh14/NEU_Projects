    private void btnCreateDrugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateDrugActionPerformed
        int selectedRow = tblWorkQueue.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a request to create drug", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        WorkRequest request = (WorkRequest) tblWorkQueue.getValueAt(selectedRow, 5);
        if (request.getStatus() == Status.INPROCESS) {
            request.setReceiver(account);
            MakeDrugJPanel makeDrugJPanel = new MakeDrugJPanel(userProcessContainer, system, request);
            userProcessContainer.add("makeDrugJPanel", makeDrugJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnCreateDrugActionPerformed

    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        int selectedRow = tblWorkQueue.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a drug to test", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (txtMessage.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter a message ", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        DrugWorkRequest request = (DrugWorkRequest) tblWorkQueue.getValueAt(selectedRow, 5);

        request.setMessage(txtMessage.getText());
        request.setSender(account);
        request.setStatus(WorkRequest.Status.TOBETESTED);
        request.setReceiver(null);

        account.getWorkQueue().getWorkQueue().add(request);
        JOptionPane.showMessageDialog(null, "The drug has been sent for pre-clinicial tests", "Information", JOptionPane.INFORMATION_MESSAGE);

        txtMessage.setText("");
    }//GEN-LAST:event_btnRequestActionPerformed

    private void btnMessageToAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMessageToAdminActionPerformed
        int selectedRow = tblWorkQueue.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a request to process", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        WorkRequest request = (WorkRequest) tblWorkQueue.getValueAt(selectedRow, 5);
        if (request.getStatus() == WorkRequest.Status.SUCCESS && request.getSender() == account) {
            ScientistApprovalJPanel approvalJPanel = new ScientistApprovalJPanel(request, account, userProcessContainer);
            userProcessContainer.add("approvalJPanel", approvalJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a successful request sent by you to approve", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnMessageToAdminActionPerformed

