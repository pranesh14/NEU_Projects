    public void populateCompounds() {
        DefaultTableModel model = (DefaultTableModel) tblCompoundHistory.getModel();

        model.setRowCount(0);
        for (Drug drug : system.getDrugList().getDrugHistory()) {
            Object[] row = new Object[3];
            row[0] = drug;
            row[1] = drug.getMolecularFormula();
            row[2] = drug.getDiscoveryDate();

            model.addRow(row);
        }
    }

    public void populateDrugWorkQueue() {
        DefaultTableModel model = (DefaultTableModel) tblWorkQueue.getModel();

        model.setRowCount(0);
        for (WorkRequest request : organization.getWorkQueue().getWorkQueue()) {
            Object[] row = new Object[6];
            row[0] = ((DrugWorkRequest) request).getDrug();
            row[1] = ((DrugWorkRequest) request).getCompound();
            row[2] = request.getSender() == null ? null : request.getSender();
            row[3] = request.getReceiver() == null ? null : request.getReceiver();
            row[4] = request.getStatus();
            row[5] = request;

            model.addRow(row);
        }
    }
