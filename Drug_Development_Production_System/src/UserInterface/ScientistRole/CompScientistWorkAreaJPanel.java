    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        int selectedRow = tblCompoundHistory.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a compound to test", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (txtMessage.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter a message ", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Compound compound = (Compound) tblCompoundHistory.getValueAt(selectedRow, 0);

        CompoundWorkRequest request = new CompoundWorkRequest();
        request.setCompound(compound);
        request.setMessage(txtMessage.getText());
        request.setSender(account);
        request.setStatus(Status.TOBETESTED);

        organization.getWorkQueue().getWorkQueue().add(request);
        account.getWorkQueue().getWorkQueue().add(request);

        JOptionPane.showMessageDialog(null, "The compound has been sent for pre-clinical tests", "Information", JOptionPane.INFORMATION_MESSAGE);

        populateWorkQueue();

        txtMessage.setText("");

    }//GEN-LAST:event_btnRequestActionPerformed

    private void btnMessageToAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMessageToAdminActionPerformed
        int selectedRow = tblWorkQueue.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a request to process", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        CompoundWorkRequest request = (CompoundWorkRequest) tblWorkQueue.getValueAt(selectedRow, 4);
        if (request.getStatus() == Status.SUCCESS && request.getSender() == account) {
            ScientistApprovalJPanel approvalJPanel = new ScientistApprovalJPanel(request, account, userProcessContainer);
            userProcessContainer.add("approvalJPanel", approvalJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a successful request sent by you to approve", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnMessageToAdminActionPerformed

