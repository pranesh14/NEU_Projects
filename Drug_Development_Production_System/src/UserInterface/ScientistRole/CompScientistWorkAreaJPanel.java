    public void populateCompoundHistory() {
        DefaultTableModel model = (DefaultTableModel) tblCompoundHistory.getModel();

        model.setRowCount(0);
        for (Compound compound : system.getCompoundList().getCompoundHistory()) {
            Object[] row = new Object[3];
            row[0] = compound;
            row[1] = compound.getMolecularFormula();
            row[2] = compound.getDiscoveryDate();

            model.addRow(row);
        }
    }

    public void populateWorkQueue() {
        DefaultTableModel model = (DefaultTableModel) tblWorkQueue.getModel();

        model.setRowCount(0);
        for (WorkRequest request : organization.getWorkQueue().getWorkQueue()) {
            Object[] row = new Object[5];
            row[0] = ((CompoundWorkRequest) request).getCompound();
            row[1] = request.getSender() == null ? null : request.getSender();
            row[2] = request.getReceiver() == null ? null : request.getReceiver();
            row[3] = request.getStatus();
            row[4] = request;

            model.addRow(row);
        }
    }
