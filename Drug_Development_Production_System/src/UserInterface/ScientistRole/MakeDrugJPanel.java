    private void populateInActiveCompounds() {
        DefaultTableModel model = (DefaultTableModel) tblCompoundList.getModel();

        model.setRowCount(0);
        for (Compound compound : system.getCompoundList().getCompoundHistory()) {
            if (!compound.isLeadComponent()) {
                Object[] row = new Object[3];
                row[0] = compound;
                row[1] = compound.getMolecularFormula();
                row[2] = compound.getDescription();

                model.addRow(row);
            }
        }
    }

    private void populateAddedCompounds() {
        DefaultTableModel model = (DefaultTableModel) tblAddedCompoundList.getModel();
        model.setRowCount(0);
        Object[] row = new Object[3];
        row[0] = compound;
        row[1] = compound.getMolecularFormula();

        model.addRow(row);
        txtMolecularFormula.setText(compound.getMolecularFormula());
    }
