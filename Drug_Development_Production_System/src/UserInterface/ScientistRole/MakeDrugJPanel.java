    private void btnAddCompoundActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCompoundActionPerformed
        int selectedRow = tblCompoundList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a compound to add to drug", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Compound compound = (Compound) tblCompoundList.getValueAt(selectedRow, 0);
        DefaultTableModel model = (DefaultTableModel) tblAddedCompoundList.getModel();

        Object[] row = new Object[3];
        row[0] = compound;
        row[1] = compound.getMolecularFormula();
        model.addRow(row);

        txtMolecularFormula.setText(txtMolecularFormula.getText() + compound.getMolecularFormula());
    }//GEN-LAST:event_btnAddCompoundActionPerformed

    private void btnCreateDrugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateDrugActionPerformed
        Drug drug = system.getDrugList().createDrug();
        drug.setName(txtName.getText());
        drug.setDiscoveryDate(new Date());
        drug.setMolecularFormula(txtMolecularFormula.getText());
        int rows = tblAddedCompoundList.getRowCount();
        double weight = 0;
        for (int i = 0; i < rows; i++) {
            Compound c = (Compound) tblAddedCompoundList.getValueAt(i, 0);
            if (c.isLeadComponent()) {
                drug.setDescription(c.getDescription());
            }
            weight += c.getMolecularWeight();
            drug.getCompoundList().add(c);
        }
        drug.setMolecularWeight(weight);

        request.setDrug(drug);

        JOptionPane.showMessageDialog(null, "Drug has been created", "Information", JOptionPane.INFORMATION_MESSAGE);

        txtMolecularFormula.setText("");
        txtName.setText("");
        DefaultTableModel model = (DefaultTableModel) tblAddedCompoundList.getModel();
        model.setRowCount(0);
    }//GEN-LAST:event_btnCreateDrugActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        int total = userProcessContainer.getComponentCount();
        DrugScientistWorkAreaJPanel manageProductCatalog = (DrugScientistWorkAreaJPanel) userProcessContainer.getComponent(total - 2);
        manageProductCatalog.populateDrugWorkQueue();
        manageProductCatalog.populateCompounds();

        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnRemoveCompoundActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveCompoundActionPerformed
        int selectedRow = tblAddedCompoundList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a compound to remove from drug combination", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Compound compound = (Compound) tblAddedCompoundList.getValueAt(selectedRow, 0);
        DefaultTableModel model = (DefaultTableModel) tblAddedCompoundList.getModel();
        if (!compound.isLeadComponent()) {
            model.removeRow(selectedRow);
        } else {
            JOptionPane.showMessageDialog(null, "Lead component cannot be deleted", "Error", JOptionPane.ERROR_MESSAGE);
        }

        int rows = tblAddedCompoundList.getRowCount();
        txtMolecularFormula.setText("");
        for (int i = 0; i < rows; i++) {
            Compound c = (Compound) tblAddedCompoundList.getValueAt(i, 0);
            txtMolecularFormula.setText(txtMolecularFormula.getText() + c.getMolecularFormula());
        }
    }//GEN-LAST:event_btnRemoveCompoundActionPerformed


