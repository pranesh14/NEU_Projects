    private void comboCompoundActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboCompoundActionPerformed
        String diseases = "";
        for(Gene gene:((Compound)comboCompound.getSelectedItem()).getDisease().getAffectedGenes()){
            if(network==null)
            for(Disease disease: system.getDiseaseList().getDiseaseCatalog()){
                for(Gene genes: disease.getAffectedGenes()){
                    if(gene == genes && disease!= ((Compound)comboCompound.getSelectedItem()).getDisease()){
                        diseases = diseases + disease.getName()+",";
                    }
                }
            }
            else
            for(Disease disease: network.getDiseaseList().getDiseaseCatalog()){
                for(Gene genes: disease.getAffectedGenes()){
                    if(gene == genes){
                        diseases = disease.getName()+",";
                    }
                }
            }
        }
        diseases = diseases.substring(0, diseases.length() - 1);
        txtAnalysis.setText("The selected compound is effective in curing "+ ((Compound)comboCompound.getSelectedItem()).getDisease().getName()+" . On further analysis, this lead compound might be effective in treating disease(s) such as "+diseases+" since they affect common genes.");
    }//GEN-LAST:event_comboCompoundActionPerformed

