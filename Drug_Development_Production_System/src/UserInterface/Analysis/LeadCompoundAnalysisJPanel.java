    private void populateCombo() {
        if(network == null)
            for(Compound compound: system.getCompoundList().getCompoundHistory()){
                if(compound.isLeadComponent())
                    comboCompound.addItem(compound);
            }
        else{
            for(Compound compound: network.getCompoundList().getCompoundHistory()){
                if(compound.isLeadComponent())
                    comboCompound.addItem(compound);
            }
        }
    }
