    public void populateMedicineList() {
        DefaultTableModel model = (DefaultTableModel) tblMedicineList.getModel();
        model.setRowCount(0);
        for (Medicine medicine : business.getMedicineCatalog().getMedicineList()) {
            Object[] row = new Object[3];
            row[0] = medicine;
            row[1] = medicine.getForm();
            row[2] = medicine.getUnitPrice();
            model.addRow(row);
        }
    }

