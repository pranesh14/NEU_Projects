    private void btnAddtoRequestTabletsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddtoRequestTabletsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMedicineList.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a medicine for purchase request", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(txtCountField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter the quantity", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
       
            Medicine medicine = (Medicine)tblMedicineList.getValueAt(selectedRow, 0);
            int count = parseInt(txtCountField.getText());
            DefaultTableModel model = (DefaultTableModel) tblRequestingMedicine.getModel();
            Object[] row = new Object[4];
            row[0] = medicine;
            row[1] = medicine.getForm();
            row[2] = medicine.getUnitPrice();
            row[3] = txtCountField.getText();
            model.addRow(row);
            txtCountField.setText("");

    }//GEN-LAST:event_btnAddtoRequestTabletsActionPerformed

