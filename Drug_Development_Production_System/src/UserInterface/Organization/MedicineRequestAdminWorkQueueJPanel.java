    private void btnRequestTabletsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestTabletsActionPerformed
        // TODO add your handling code here:
        int rowCount = tblRequestingMedicine.getRowCount();
        for(int i=0; i<rowCount; i++){
            PurchaseOrderWorkRequest workRequest = new PurchaseOrderWorkRequest();
            Medicine medicine = (Medicine)tblRequestingMedicine.getValueAt(i, 0);
            workRequest.setMedicine(medicine);
            workRequest.setMedicineCount(parseInt((String) tblRequestingMedicine.getValueAt(i, 3)));
            workRequest.setSender(userAccount);
            workRequest.setReceiver(null);
            workRequest.setMessage(txtMessage.getText());
            workRequest.setStatus(WorkRequest.Status.PURCHASEREQUEST);
            organization.getWorkQueue().getWorkQueue().add(workRequest);
            enterprise.getWorkQueue().getWorkQueue().add(workRequest);
        }
        JOptionPane.showMessageDialog(null, "Order Placed, You can check order status by clicking 'check order status' ");
        DefaultTableModel model = (DefaultTableModel) tblMedicineList.getModel();
        model.setRowCount(0);
    }//GEN-LAST:event_btnRequestTabletsActionPerformed


