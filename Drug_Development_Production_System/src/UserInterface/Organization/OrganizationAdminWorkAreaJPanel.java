    private void btnViewWorkQueueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewWorkQueueActionPerformed
        // TODO add your handling code here:
        if (organization.getOrgType() == Organization.OrganizationType.CompoundResearch) {
            CompOrgAdminWorkQueueJPanel compOrgAdminWorkQueue = new CompOrgAdminWorkQueueJPanel(userProcessContainer, organization, enterprise, userAccount);
            userProcessContainer.add("compOrgAdminWorkQueue", compOrgAdminWorkQueue);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        if (organization.getOrgType() == Organization.OrganizationType.DrugResearch) {
            DrugOrgAdminWorkQueueJPanel drugOrgAdminWorkQueue = new DrugOrgAdminWorkQueueJPanel(userProcessContainer, organization, enterprise, userAccount, business);
            userProcessContainer.add("drugOrgAdminWorkQueue", drugOrgAdminWorkQueue);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        if (organization.getOrgType() == Organization.OrganizationType.ClinicalTrials) {
            ClinicalOrgAdminWorkQueueJPanel clinicalOrgAdminWorkQueue = new ClinicalOrgAdminWorkQueueJPanel(userProcessContainer, organization, enterprise, userAccount, business);
            userProcessContainer.add("clinicalOrgAdminWorkQueue", clinicalOrgAdminWorkQueue);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }

        if (organization.getOrgType() == Organization.OrganizationType.Marketing) {
            MedicineRequestAdminWorkQueueJPanel medicineRequestAdminWorkQueue = new MedicineRequestAdminWorkQueueJPanel(userProcessContainer, organization, enterprise, userAccount, business);
            userProcessContainer.add("medicineRequestAdminWorkQueue", medicineRequestAdminWorkQueue);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewWorkQueueActionPerformed


