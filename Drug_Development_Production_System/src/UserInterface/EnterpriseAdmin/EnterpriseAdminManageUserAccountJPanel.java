    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtUserName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter User Name", "Error", JOptionPane.ERROR_MESSAGE);
        }
        if (txtPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter Password", "Error", JOptionPane.ERROR_MESSAGE);
        }

        if (!organization.getUserAccountDirectory().checkIfUsernameIsUnique(txtUserName.getText())) {
            JOptionPane.showMessageDialog(null, "Username exists. Please enter a different user name.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        UserAccount ua = organization.getUserAccountDirectory().createUserAccount();
        ua.setUsername(txtUserName.getText());
        ua.setPassword(txtPassword.getText());
        ua.setEmployee(emp);
        switch (organization.getOrgType()) {
            case DrugResearch:
                ua.setRole(new DrugResearchOrganizationAdminRole());
                break;
            case ClinicalTrials:
                ua.setRole(new ClinicalTrialsOrganizationAdminRole());
                break;
            case CompoundResearch:
                ua.setRole(new CompoundResearchOrganizationAdminRole());
                break;
            case Marketing:
                ua.setRole(new MarketingOrganizationAdminRole());
                break;
            default:
                break;
        }

        JOptionPane.showMessageDialog(null, "User Account has been created", "Information", JOptionPane.INFORMATION_MESSAGE);
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 3];
        EnterpriseAdminWorkAreaJPanel panel = (EnterpriseAdminWorkAreaJPanel) component;
        userProcessContainer.add(panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnSaveActionPerformed


